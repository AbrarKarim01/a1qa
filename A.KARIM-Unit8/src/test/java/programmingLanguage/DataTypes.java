package programmingLanguage;

public class DataTypes {

    public static void main(String[] args) {
        int num1 = 10;
        long longNum = 176;
        float floatNum = 4.8f;
        double bigNum = 5.9;
        char name = 'A';
        String str = "Hello";
        boolean isTrue = true;

        Integer num2 = 10; // Wrapper class
        Long longNum2 = 176L;
        Float floatNum2 = 4.8f;
        Double bigNum2 = 5.9;
        Character name2 = 'A';
        String str2 = "Hello";
        Boolean isTrue2 = true;

        System.out.println("int: " + num1 + " long: " + longNum + " float: " + floatNum + " double: " + bigNum + " char: " + name + " String: " + str + " boolean: " + isTrue);
        System.out.println("Integer: " + num2 + " Long: " + longNum2 + " Float: " + floatNum2 + " Double: " + bigNum2 + " Character: " + name2 + " String: " + str2 + " Boolean: " + isTrue2);
    }
}
